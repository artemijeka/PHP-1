function p($val, $pow) { // p(2, 3) 
    $res = 1; // Начальное значение результата...
    while ($pow >= 1) { // y y y n
        $res *= $val; // 2 4 8
        p($val, --$pow); // p(2, 2) p(2, 1) p(2, 0)
    }
    return $res; // n n n 8
}
